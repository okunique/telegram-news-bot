            "–Ø –±—É–¥—É —Å–æ–±–∏—Ä–∞—Ç—å –Ω–æ–≤–æ—Å—Ç–∏ –∏–∑ —É–∫–∞–∑–∞–Ω–Ω—ã—Ö –∫–∞–Ω–∞–ª–æ–≤, "
            "–ø–µ—Ä–µ–≤–æ–¥–∏—Ç—å –∏—Ö –∏ —Å–æ–∑–¥–∞–≤–∞—Ç—å –∞–Ω–∞–ª–∏—Ç–∏—á–µ—Å–∫–∏–µ –¥–∞–π–¥–∂–µ—Å—Ç—ã."
